# Inch to Foot

# 1 inch = 0.0833333 feet
# 👉 Formula:
# feet = inches ÷ 12

def InchToFoot(inch):
    f = inch / 12
    return f
# 📐 Inch to Meter

# 1 inch = 0.0254 meters
# 👉 Formula:
# meters = inches × 0.0254

def InchToMeter(inch):
    m = inch  * 0.0254
    return m
# 🌍 Inch to Kilometer

# 1 inch = 0.0000254 km
# 👉 Formula:
# km = inches × 0.0000254
def InchToKm(inch):
    k = inch  * 0.0000254
    return k
# 🛣️ Kilometer to Mile

# 1 km = 0.621371 miles
# 👉 Formula:
# miles = km × 0.621371

def KmToMiles(km):
    mi = km  * 0.621371
    return mi